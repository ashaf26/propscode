<script setup>
import { onMounted } from 'vue'

onMounted(() => {
  const container = document.querySelector('.row')
  const cardWidth = document.querySelector('.col').offsetWidth

  container.addEventListener('wheel', (event) => {
    event.preventDefault()
    container.scrollBy({ left: event.deltaY > 0 ? cardWidth : -cardWidth, behavior: 'smooth' })
  })

  let currentIndex = 0
  const row = document.getElementById('row')
  const cards = document.querySelectorAll('.card')
  let autoScrollInterval
  let userInteracting = false
  let timeoutId

  function scrollNextCard() {
    if (!userInteracting) {
      currentIndex = (currentIndex + 1) % cards.length // Loop kembali ke awal
      const scrollLeft = cards[currentIndex].offsetLeft
      row.scrollTo({ left: scrollLeft, behavior: 'smooth' })
    }
  }

  // Fungsi untuk memulai kembali auto-scroll setelah beberapa detik tanpa interaksi
  function restartAutoScroll() {
    clearTimeout(timeoutId)
    timeoutId = setTimeout(() => {
      userInteracting = false
      startAutoScroll()
    }, 5000) // Jeda 5 detik sebelum auto-scroll berjalan lagi
  }

  // Fungsi untuk memulai auto-scroll
  function startAutoScroll() {
    clearInterval(autoScrollInterval)
    autoScrollInterval = setInterval(scrollNextCard, 3000)
  }

  // Fungsi untuk menghentikan auto-scroll saat ada interaksi pengguna
  function stopAutoScroll() {
    userInteracting = true
    clearInterval(autoScrollInterval)
    restartAutoScroll() // Coba mulai lagi setelah jeda
  }

  // Event listener untuk mendeteksi interaksi pengguna
  row.addEventListener('touchstart', stopAutoScroll)
  row.addEventListener('mousedown', stopAutoScroll)
  row.addEventListener('wheel', stopAutoScroll)

  // Mulai auto-scroll saat halaman dimuat
  startAutoScroll()
})
</script>

